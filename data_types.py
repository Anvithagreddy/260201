# floats
# a float can be added to an integer
print(8/2)
print(6 * 7.0)
print(4 + 1.65)

# exponentiation
# the result will be float
print("Exponentiation")
print(2 ** 5) # (2 power of 5)
print(9 ** (1/2))
print(2 ** 2 ** 2)

# Quotient
# // is used to determine the quotient of a division
print("Print only quotient")
print(20 // 6)
print(40 // 2)

# reminder
# % is used to get reminder
print("Print reminder")
print(20 % 6)
print(1.25 % .5)
