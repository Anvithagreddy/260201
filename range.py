# To create number sequences using the range()
# by default, it starts from 0, increments by 1 and stops brfore the specified number
print("RANGE")
numbers = list(range(10))
print(numbers)

# range with two arguments
print("\n\nRANGE WITH TWO ARGS")
numbers = list(range(3,8))
print(numbers)
# it prints from 3 to 7
